<b><p> <font size="4"> Field Goal Attempted (FGA): </p></b> </font>
<p> Field Goals are baskets attempted on the net on any shot or hit other than a free throw and can be worth two or three points depending from where the shot is taken.
</p>
<b><p> <font size="4"> Field Goals Made (FGM): </p></b> </font>
<p>
Field goals that have successfully entered the net and accounted for a point.
</p>
<b><p> <font size="4"> Field Goal Percent (FG%): </p></b> </font>
<p>
The ratio of field goals scored over the field goals attempted at net.
</p>
<b><p> <font size="4"> Free Throws Attempted (FTA): </p></b> </font>
<p>
<p>
Field throws are unimpeded attempt at a shot in net that is worth one point and awarded to a player when they are fouled or infringed.
</p>
<b><p> <font size="4"> Free Throws Made (FTM): </p></b> </font>
<p>
<p>Field throws made are field throws are successfully enter the net and players achieve 1 point.
</p>
<b><p> <font size="4"> Free Throw Percentage (FT%): </p></b> </font>
<p>
<p>Field throw percentages are the summation of field throws successfully made over fields attempted.
</p>
<b><p> <font size="4"> Three Points Attempted (3PTA): </p></b> </font>
<p>
<p>Three points attempted are shots attempt at the net made beyond the three point line.
</p>
<b><p> <font size="4"> Three Points Made (3PTM):</p></b> </font>
<p>
<p> Successful shots made into the net from the three-point line or past it.</p>
<b><p> <font size="4"> Three Point Percentage (3PT%): </p></b> </font>
<p>
<p>Ratio of three-point shots scored over attempted.
</p>
<b><p> <font size="4"> Total Rebounds: </p></b> </font>
<p>
<p>Statistical award to a player that grabs a missing shot
</p>
<b><p> <font size="4"> Total Assists: </p></b> </font>
<p>
<p>Statistical award to a player that passes a ball to a player before shot is successfully made.
</p>
<b><p> <font size="4"> Total Steals: </p></b> </font>
<p>
<p>Statistical award to a player that steals a ball from the opposing team.
</p>
<b><p> <font size="4"> Total Blocks: </p></b> </font>
<p>
<p>Statistical award to a player that prevents the other team from scoring a point.
