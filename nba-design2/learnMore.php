<span class="black-text text-darken-6">

<br>
<p>
The National Basketball Association was founded in 1946 by the Basketball Association of America. The NBA is men’s professional basketball association is composed of 30 teams, 29 represent American teams and 1 is from Canada as the Toronto Raptors. There are about 20 players per team and NBA players are the world’s best paid athletes per annual salary.
<br>
</p>
<p>
Each team is divided into the Western and Eastern Conference. The Regular NBA Season runs from the last week of October to the middle of April. During the regular season, each team plays 82 games, 41 games at home and 41 games away. Teams face opponents in their own division four times per year to account for 16 games and play six teams from the other two divisions four times to account for 24 games. The remaining four teams are played three times to account for 12 games.
<br>
</P>
<p>
The NBA All-Star Game is celebrated in February. Fans vote for which players they will like to see play during the All-Star Game. The All-Star Weekend is composed of the Rising Stars Challenge where top rookies and second NBA players play a 5-on-5 basketball game. This activity includes the Skills Challenge where players participate in a obstacle course consisting of shooting, passing and dribbling. During the All-Star Weekend players also have a Three Point Contest where players shoot the highest number of 3-point shots in a given period of time from different locations across the 3-point field. Finally, the NBA Slam Dunk Contest is the most watched Pre All-Star Game ritual. During this event, players compete to dunk the ball in an entertaining manner to impress judges.
<br>
</p>
<p>
The NBA awards multiple honors at the end of the season. These awards are special recognitions to players that have made an impressive effort during the regular and play off season. This includes: The Sixth Man of the Year, the Rookie of the Year, The Most Improved Player =, The Defensive Player of the Year, the Coach of the Year, the Most Valuable Player as well as the Executive of the Year. Team awards are also offered including the All-NBA Team, the All-Defensive Team and All-Rookie Team.
